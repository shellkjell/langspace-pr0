// Package pkg contains the core logic for LangSpace.
//
// LangSpace is a declarative language for composing AI agent workflows.
// This directory contains the library components used by the LangSpace CLI
// and which can be used by other Go applications to integrate LangSpace.
//
// Main components:
//   - ast: Abstract Syntax Tree definitions
//   - parser: Language parser and tokenizer
//   - validator: Entity validation
//   - workspace: Workspace and relationship management
//   - runtime: Execution engine and LLM integration
//   - compile: Code generation for target languages
package pkg
